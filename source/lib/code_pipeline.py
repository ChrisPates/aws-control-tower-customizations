#!/bin/python
import boto3
import inspect

class CodePipeline(object):
    def __init__(self, logger, **kwargs):
        self.logger = logger
        if kwargs is not None:
            if kwargs.get('credentials') is None:
                logger.debug("Setting up code pipeline BOTO3 Client with default credentials")
                self.cp = boto3.client('codepipeline')
            else:
                logger.debug("Setting up code pipeline BOTO3 Client with ASSUMED ROLE credentials")
                cred = kwargs.get('credentials')
                self.cp = boto3.client('codepipeline',
                                               aws_access_key_id=cred.get('AccessKeyId'),
                                               aws_secret_access_key=cred.get('SecretAccessKey'),
                                               aws_session_token=cred.get('SessionToken')
                                               )
        else:
            logger.info("There were no keyworded variables passed.")
            self.cp = boto3.client('codepipeline')
            
    def start_pipeline_execution(self, code_pipeline_name):
        try:
            response = self.cp.start_pipeline_execution(
                name=code_pipeline_name
            )
            return response
        except Exception as e:
            message = {'FILE': __file__.split('/')[-1], 'CLASS': self.__class__.__name__,
                       'METHOD': inspect.stack()[0][3], 'EXCEPTION': str(e)}
            self.logger.exception(message)
            raise     